package com.example.aakash.ui.comment;

public class CommentHelpModel {
    private static String oldUsersId, oldUsername, oldCommentId;

    public CommentHelpModel() {}

    public String getOldUsersId() {
        return oldUsersId;
    }

    public void setOldUsersId(String oldUsersId) {
        this.oldUsersId = oldUsersId;
    }

    public String getOldUsername() {
        return oldUsername;
    }

    public void setOldUsername(String oldUsername) {
        this.oldUsername = oldUsername;
    }

    public String getOldCommentId() {
        return oldCommentId;
    }

    public void setOldCommentId(String oldCommentId) {
        this.oldCommentId = oldCommentId;
    }
}
